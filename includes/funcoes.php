<?php

	function DiasSemana($dia){
	    switch($dia){
	        case 0:{
	            return 'dom';
	            break;
	        }
	        case 1:{
	            return 'seg';
	            break;
	        }
	        case 2:{
	            return 'ter';
	            break;
	        }
	        case 3:{
	            return 'qua';
	            break;
	        }
	        case 4:{
	            return 'qui';
	            break;
	        }
	        case 5:{
	            return 'sex';
	            break;
	        }
	        case 6:{
	            return 'sab';
	            break;
	        }
	    }
	}
	
	
?>